from __future__ import annotations


SYSTEM_PROMPT = """Ты извлекаешь задачи из русскоязычных транскриптов встреч.

Работай как слой смыслового извлечения данных, а не как чат-ассистент.
Верни только данные, соответствующие JSON-схеме.

Критичные правила:
1. Одна строка результата = одна задача в одном блоке.
2. Блоки только: "Выполненные", "Невыполненные", "Новые".
3. "Выполненные": явно сказано, что работа сделана, закрыта, отправлена, утверждена, завершена.
4. "Невыполненные": явно сказано, что задача не выполнена, не успели, просрочили, срок прошел, задача сорвана или не закрыта.
5. Не относить к "Невыполненные", если задача пока не сделана, но срок еще не наступил.
6. "Новые": новое поручение или продолжение невыполненной работы только если есть конкретный срок.
7. Не добавляй в "Новые" задачи без срока.
8. Сроки не придумывай. Если срока нет, deadline_raw = "".
9. responsible = явно названный ответственный из evidence, если есть фраза вида "ответственный/ответственная ...".
10. Слово "ответственность" не назначает ответственного. Это не маркер responsible.
11. Если явного ответственного нет, responsible равен говорящему из реплики-обоснования. Не бери человеком, которого просто упомянули внутри реплики без назначения ответственным.
12. evidence должен быть дословной цитатой из транскрипта с именем спикера.
13. В evidence должна быть прямая связь с задачей, статусом и сроком, если срок есть.
14. Для "Выполненные" всегда deadline_raw = "". Даты в соседнем контексте не являются сроком выполненной задачи.
15. Не добавляй галлюцинации: если прямого подтверждения нет в transcript, задачи нет.
16. Не объединяй разные задачи в одну строку.
17. Удаляй явные дубли одной и той же задачи.
18. Не извлекай как задачи обычные обсуждения, идеи, контекст проекта и описания текущего состояния.
19. Не добавляй в "Невыполненные" фразы вида "осталось сделать", "будем делать", "ожидаем", "занимаюсь", если нет явного смысла провала или просрочки.
20. Не добавляй в "Новые" текущие работы и состояния вроде "производится", "начали", "уже проводим", "сейчас", если нет явного поручения.
21. Не добавляй в "Новые" будущие планы без конкретного срока.
22. Каждая задача обязана ссылаться на один или несколько anchor id из входа.
23. Не используй строки вне переданных anchor-блоков.
24. Если anchor не содержит задачу, не возвращай его.
25. Anchor-блоки high-recall: они могут содержать шумный контекст. Не нужно извлекать задачу из каждого anchor.
26. Поле task формулируй максимально словами из evidence: сохраняй ключевые глаголы и существительные из цитаты, не заменяй их синонимами.
27. В "Выполненные" добавляй только завершенные действия с объектом работы. Не добавляй описания состояния, готовности, ожидания, будущих действий или фразы "осталось", "будем", "ожидаем", "прорабатывается".
28. Не добавляй в "Выполненные" неполные строки вида "Борис подготовил", "написано и сделано", "разработаны собственно", если из evidence не понятно, какой объект работы завершен.
29. Не добавляй в "Выполненные" отрицательные статусы вида "не были изготовлены", "не были смонтированы", "не выполнено", даже если рядом есть done-глагол.
30. Не размножай summary-строки из одной цитаты: если в одной evidence-фразе несколько общих фраз о выполнении близких объектов, верни максимум одну наиболее информативную строку.
31. Не добавляй в "Выполненные" строки вида "есть накопление", "всё сделано", "всё готово", "она готова", "в целом мы готовы", если из evidence не понятно, какая конкретная задача завершена.

Перед добавлением каждой строки мысленно проверь:
- Есть ли в evidence прямое подтверждение блока?
- Для "Выполненные" есть ли конкретный завершенный объект работы, а не просто состояние или план?
- Для "Новые" есть ли в evidence конкретный срок?
- responsible либо явно назначен ответственным в evidence, либо совпадает с говорящим в evidence?
- Можно ли проверяющему по evidence понять задачу без догадок?
- Повторяет ли task ключевые слова из evidence, а не пересказывает их синонимами?
- anchor_ids существуют во входе?
"""


def build_user_prompt(meeting_date: str, transcript: str) -> str:
    return f"""Дата встречи: {meeting_date}

Ниже даны anchor-блоки: детерминированно выбранные релевантные фрагменты
исходного транскрипта встречи. Каждый anchor имеет id, тип сигнала, speaker,
номера строк и найденные фразы сроков.

Извлеки задачи только из этих anchor-блоков.

Верни:
- выполненные задачи;
- невыполненные задачи;
- новые задачи со сроком.

Поле task пиши коротко, но лексически близко к evidence: используй слова из
цитаты. Например, если в evidence сказано "13 мая соберемся", не пиши
"назначить встречу", пиши "собраться 13 мая" или близко к этой формулировке.

Не нормализуй даты сам в YYYY-MM-DD. В поле deadline_raw положи дословную фразу срока
из anchor, например "завтра", "до пятницы", "17 апреля", "через месяц".
Если срока нет, deadline_raw оставь пустой строкой.

Для каждой задачи обязательно укажи anchor_ids: список id anchor-блоков,
которые подтверждают задачу.

Anchor-блоки:
{transcript}
"""


def build_anchor_decision_user_prompt(meeting_date: str, transcript: str) -> str:
    return f"""Дата встречи: {meeting_date}

Ниже даны anchor-блоки: детерминированно выбранные релевантные фрагменты
исходного транскрипта встречи. Каждый anchor имеет id, тип сигнала, speaker,
номера строк и найденные фразы сроков.

Нужно принять отдельное решение по каждому anchor из входа.

Верни JSON с полем anchor_decisions:
- один объект на каждый anchor из входа;
- anchor_id должен совпадать с id входного anchor;
- если в anchor нет подтвержденных задач, верни tasks: [];
- если задача подтверждена соседними anchors той же группы, укажи все
  подтверждающие anchor_ids внутри task.

Для каждой найденной задачи действуют те же правила:
- блок только "Выполненные", "Невыполненные" или "Новые";
- "Новые" добавляй только при конкретном сроке в evidence;
- "Выполненные" всегда deadline_raw = "";
- task формулируй коротко и словами из evidence, без синонимизации;
- evidence должен быть дословной цитатой из переданного anchor-блока с именем
  спикера;
- не извлекай задачу, если по evidence проверяющий не сможет подтвердить блок,
  задачу, ответственного и срок.

Важно: не пропускай anchor_decision для anchor только потому, что задач нет.
В этом случае обязательно верни пустой список tasks.

Anchor-блоки:
{transcript}
"""


def build_verifier_user_prompt(meeting_date: str, candidates: str) -> str:
    return f"""Дата встречи: {meeting_date}

Ниже кандидаты задач, которые появились не во всех LLM-прогонах, но имеют
частичную поддержку. Твоя задача - только проверить evidence. Не ищи новые
задачи и не исправляй формулировки.

Для каждого candidate_id верни решение:
- keep=true, если evidence прямо подтверждает блок, задачу, ответственного и
  срок;
- keep=false, если требуется догадка, evidence слишком общее, нет объекта
  работы, нет срока для "Новые", статус не относится к задаче или есть риск
  галлюцинации.

Правила проверки:
1. "Выполненные": нужен явный completed-статус и конкретный объект работы.
   Reject для общих фраз вроде "всё сделано", "готово", "я подготовил",
   если не понятно, что именно завершено.
2. "Невыполненные": нужен явный failed-статус и конкретный объект задачи.
   Reject для фраз вроде "я просто не успел" без понятного объекта.
3. "Новые": нужен явный assignment/future action и конкретный срок в evidence.
4. Срок в candidate.deadline должен подтверждаться evidence.
5. Ответственный должен быть либо говорящим в evidence, либо явно назначенным
   ответственным в evidence.
6. Не используй знания вне evidence.

Кандидаты:
{candidates}
"""
